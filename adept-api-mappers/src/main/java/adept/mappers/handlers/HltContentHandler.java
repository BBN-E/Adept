package adept.mappers.handlers;

import thrift.adept.common.*;


/**
 * The Class HltContent is an abstract class that represents all the HLT content
 * generated by ADEPT algorithms. All algorithmic output types inherits from
 * HltContent.
 */
public abstract class HltContentHandler extends ItemHandler implements HltContentService.Iface {

	private HltContent myHltContent;

	/**
	 * Instantiates a new hlt content.
	 */
	public HltContentHandler() {
		super();
		myHltContent = new HltContent();
		// TODO Auto-generated constructor stub
	}
	public HltContent getHltContent() {
		return myHltContent;
	}

}
